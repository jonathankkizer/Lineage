
  
    

create or replace transient table dw_dev.dev_jkizer.intmdt_med_adherence
    copy grants
    
    
    as (with cte_union_med as ( 
	select
        member_id,
        quality_measure,
        quality_measure_type,
        measure_weight,
        lis_level,
        payer_quality_measure,
        payer_suvida_measure_match,
        measure_detail,
        to_number(perc_days_covered, 10, 4) as perc_days_covered,
        rx_name,
        is_single_fill,
        rx_number,
        ninety_day_opportunity,
        gap_days_remaining,
        member_status,
        prescriber_name,
        prescriber_phone,
        last_fill_day_supply,
        last_fill_date,
        next_refill_due,
        refills_remaining,
        rx_tier,
        first_fill_date,
        number_of_fills,
        pharmacy_name,
        pharmacy_phone,
        pharmacy_address,
        null as measure_program,
        null as payer_group,
        report_date,
        null as measure_year,
        'UHG/Wellmed' AS measure_source,
        src_file_name,
        report_type,
        false as split_mad_by_drug,
        null::varchar as drug_name_category,
        null as claim_reversal,
        null as absolute_fail_date,
    from dw_dev.dev_jkizer_staging.stg_wellmed_med_adherence
    
    union all
    
    select
        member_id,
        quality_measure,
        quality_measure_type,
        measure_weight,
        null as lis_level,
        payer_quality_measure,
        payer_suvida_measure_match,
        measure_detail,
        to_number(perc_days_covered, 10, 4) as perc_days_covered,
        rx_name,
        is_single_fill,
        rx_number,
        ninety_day_opportunity,
        gap_days_remaining,
        member_status,
        prescriber_name,
        prescriber_phone,
        last_fill_day_supply,
        last_fill_date,
        next_refill_due,
        refills_remaining,
        rx_tier,
        first_fill_date,
        number_of_fills,
        pharmacy_name,
        pharmacy_phone,
        pharmacy_address,
        null as measure_program,
        null as payer_group,
        report_date,
        measure_year,
        'Wellcare/Centene' as measure_source,
        src_file_name,
        report_type,
        false as split_mad_by_drug,
        null::varchar as drug_name_category,
        claim_reversal,
        null as absolute_fail_date,
    from dw_dev.dev_jkizer_staging.stg_wellcare_med_adherence
    group by all

    union all

    select
        member_id,
        quality_measure,
        quality_measure_type,
        measure_weight,
        null as lis_level,
        payer_quality_measure,
        payer_suvida_measure_match,
        measure_detail,
        to_number(perc_days_covered, 10, 4) as perc_days_covered,
        rx_name,
        is_single_fill,
        rx_number,
        ninety_day_opportunity,
        gap_days_remaining,
        member_status,
        prescriber_name,
        prescriber_phone,
        last_fill_day_supply,
        last_fill_date,
        next_refill_due,
        refills_remaining,
        rx_tier,
        first_fill_date,
        number_of_fills,
        pharmacy_name,
        pharmacy_phone,
        pharmacy_address,
        null as measure_program,
        null as payer_group,
        report_date,
        measure_year,
        'Wellcare/Centene' as measure_source,
        src_file_name,
        report_type,
        false as split_mad_by_drug,
        null::varchar as drug_name_category,
        null as claim_reversal,
        null as absolute_fail_date,
    from dw_dev.dev_jkizer_staging.stg_wellcare_national_med_adherence
    group by all

    union all

    select
        member_id,
        quality_measure,
        quality_measure_type,
        measure_weight,
        lis_level,
        payer_quality_measure,
        payer_suvida_measure_match,
        measure_detail,
        to_number(perc_days_covered, 10, 4) as perc_days_covered,
        rx_name,
        is_single_fill,
        rx_number,
        ninety_day_opportunity,
        gap_days_remaining,
        member_status,
        prescriber_name,
        prescriber_phone,
        last_fill_day_supply,
        last_fill_date,
        next_refill_due,
        refills_remaining,
        rx_tier,
        first_fill_date,
        number_of_fills,
        pharmacy_name,
        pharmacy_phone,
        pharmacy_address,
        null as measure_program,
        null as payer_group,
        report_date,
        measure_year,
        'Devoted' AS measure_source,
        src_file_name,
        report_type,
        false as split_mad_by_drug,
        null::varchar as drug_name_category,
        claim_reversal,
        null as absolute_fail_date,
    from dw_dev.dev_jkizer_staging.stg_devoted_med_adherence

    union all

    select
        to_varchar(member_id) AS member_id,
        quality_measure,
        quality_measure_type,
        measure_weight,
        lis_level,
        payer_quality_measure,
        payer_suvida_measure_match,
        measure_detail,
        to_number(perc_days_covered, 10, 4) as perc_days_covered,
        rx_name,
        case when is_single_fill = 'TRUE' then 1 else 0 end as is_single_fill,
        rx_number,
        ninety_day_opportunity,
        gap_days_remaining,
        member_status,
        prescriber_name,
        prescriber_phone,
        last_fill_day_supply,
        last_fill_date,
        next_refill_due,
        refills_remaining,
        rx_tier,
        first_fill_date,
        number_of_fills,
        pharmacy_name,
        pharmacy_phone,
        pharmacy_address,
        measure_program,
        case
            when line_of_business ilike '%dsnp%' and measure_program = 'ACO' then 'DSNP'
            when measure_program = 'ACO' and provider_group_name ilike '%tucson%' then 'ACO-TUC'
            when measure_program = 'ACO' and provider_group_name ilike '%phoenix%' then 'ACO-PHX'
            else null
        end as payer_group,
        report_date,
        null as measure_year,
        'United' AS measure_source,
        src_file_name,
        report_type,
        split_mad_by_drug,
        drug_name_category,
        null as claim_reversal,
        absolute_fail_date,
    from dw_dev.dev_jkizer_staging.stg_united_az_med_adherence
    qualify row_number() over (partition by member_id, quality_measure, src_file_name, report_type order by next_refill_due asc) = 1

    union all

    select
        member_id,
        quality_measure,
        quality_measure_type,
        measure_weight,
        null as lis_level,
        payer_quality_measure,
        payer_suvida_measure_match,
        measure_detail,
        to_number(perc_days_covered, 10, 4) as perc_days_covered,
        rx_name,
        is_single_fill,
        rx_number,
        ninety_day_opportunity,
        gap_days_remaining,
        member_status,
        prescriber_name,
        prescriber_phone,
        last_fill_day_supply,
        last_fill_date,
        next_refill_due,
        refills_remaining,
        rx_tier,
        first_fill_date,
        number_of_fills,
        pharmacy_name,
        pharmacy_phone,
        pharmacy_address,
        null as measure_program,
        null as payer_group,
        report_date,
        null as measure_year,
        'Alignment AZ' AS measure_source,
        src_file_name,
        report_type,
        false as split_mad_by_drug,
        null::varchar as drug_name_category,
        null as claim_reversal,
        null as absolute_fail_date,
    from dw_dev.dev_jkizer_staging.stg_alignment_med_adherence
), impute_measure_year as ( -- each file should contain only one measure year, which should be the year of last fills
    select
        src_file_name,
        date_from_parts(year(min(last_fill_date)), '01', '01') as measure_year,
    from cte_union_med
    group by all
)
select
    member_id,
    quality_measure,
    quality_measure_type,
    measure_weight,
    cast(lis_level as int) as lis_level,
    payer_quality_measure,
    payer_suvida_measure_match,
    measure_detail,
    is_single_fill,
    rx_name,
    rx_number,
    iff(perc_days_covered >= 80, 'closed', 'open') as measure_status,
    iff((perc_days_covered >= 80 or 
            (year(next_refill_due) = year(coalesce(cem.measure_year, imy.measure_year)) + 1 and 
            gap_days_remaining >= 0) or 
            year(dateadd(day, gap_days_remaining, next_refill_due)) = year(coalesce(cem.measure_year, imy.measure_year)) + 1), 
        'closed', 
        'open') 
    as measure_status_v2,
    iff(perc_days_covered >= 80, 1, 0) as measure_numerator,
    iff((perc_days_covered >= 80 or 
            (year(next_refill_due) = year(coalesce(cem.measure_year, imy.measure_year)) + 1 and 
            gap_days_remaining >= 0) or 
            (year(dateadd(day, gap_days_remaining, next_refill_due)) = year(coalesce(cem.measure_year, imy.measure_year)) + 1 and gap_days_remaining >= 0)), 
        1, 
        0) 
    as measure_numerator_v2,
    1 as measure_denominator,
    perc_days_covered,
    ninety_day_opportunity,
    gap_days_remaining,
    member_status,
    prescriber_name,
    prescriber_phone,
    last_fill_day_supply,
    last_fill_date,
    next_refill_due,
    refills_remaining,
    rx_tier,
    first_fill_date,
    number_of_fills,
    pharmacy_name,
    pharmacy_phone,
    pharmacy_address,
    report_date,
    coalesce(cem.measure_year, imy.measure_year) as measure_year,
    measure_source,
    cem.src_file_name,
    report_type,
    measure_program,
    payer_group,
    split_mad_by_drug,
    drug_name_category,
    claim_reversal,
    absolute_fail_date,
from cte_union_med cem
left join impute_measure_year imy
    on cem.src_file_name = imy.src_file_name
    )
;


  