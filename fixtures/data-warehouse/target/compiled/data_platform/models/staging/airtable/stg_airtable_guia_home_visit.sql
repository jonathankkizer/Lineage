with parsing_data as (
    select
        airtable_id,
        run_datetime,
        "LAST MODIFIED" as last_modified,
        airtable_data,
        parse_json(airtable_data) as j
    from  source_prod.airtable.src_airtable_guia_home_visits
),
type_conversion as (
    select
        airtable_id,
        run_datetime,
        last_modified,

        -- simple string fields
        j:"Stage"::string as home_visit_stage,
        j:"Status"::string as home_visit_status,
        try_to_date(j:"HV Completed Date"::string) as hv_completed_date,
        try_to_boolean(j:"(NCM) Discharge paperwork needed?"::string) as is_discharge_paperwork_needed,
        try_to_boolean(j:"Is discharge paperwork needed?"::string) as is_discharge_paperwork_needed_v2,
        try_to_boolean(j:"Home Visit Completed?"::string) as is_home_visit_completed,
        try_to_boolean(j:"(NCM) Confirm Discharge Date?"::string) as is_discharge_date_confirmed,
        j:"Medication Reconciliation Due?"::string as medication_reconciliation_due,
        try_to_boolean(j:"Medications available?"::string) as is_medications_available,
        j:"Medication List Photo"::string as medication_list_photo,
        j:"Discharge Summary Photo"::string as discharge_summary_photo,
        j:"Signed ROI Photo"::string as signed_roi_photo,
        try_to_boolean(j:"ROI Completed?"::string) as is_roi_completed,
        j:"Notes"::string as notes,
        j:"🧭 Compas Note"::string as compas_note,
        j:"Attachments"::string as attachments,
        j:"PCP TOC Visit Outcome"::string as pcp_toc_visit_outcome,
        j:"ROI Due Alert"::string as roi_due_alert,
        j:"⚠️Delay Reason"::string as delay_reason,
        j:"WarpButton":"url"::string as warp_button,

        -- resolution
        try_to_boolean(j:"Resolved?"::string) as is_resolved,
        try_to_timestamp_tz(j:"Resolved at"::string) as resolved_at,
        j:"Resolved by":"id"::string as resolved_by_id,
        j:"Resolved by":"email"::string as resolved_by_email,
        j:"Resolved by":"name"::string as resolved_by_name,

        -- array fields (take first element)
        j:"Task Master (LINK)"[0]::string as task_master_link_id,
        j:"Patient (LINK)"[0]::string as patient_link_id,
        j:"Patient Name"[0]::string as patient_name,
        j:"Workflow Type"[0]::string as workflow_type,
        try_to_date(j:"Due Date"[0]::string) as due_date,
        try_to_timestamp_tz(j:"Request Date"[0]::string) as request_datetime,
        j:"Requestor Notes"[0]::string as requestor_notes,
        j:"location_name (from Patient (LINK))"[0]::string as location_name,
        j:"provider_name (from Patient (LINK))"[0]::string as provider_name,
        j:"elation_id (from Patient (LINK))"[0]::string as elation_id,
        j:"elation_patient_url (from Patient (LINK))"[0]::string as elation_patient_url,
        try_to_date(j:"roi_form_due_date (from Patient (LINK))"[0]::string) as roi_form_due_date,
        j:"is_active_assignment (from Patient (LINK))"[0]::string as is_active_assignment,
        j:"high_risk_patient (from Patient (LINK))"[0]::string as high_risk_patient,
        try_to_date(j:"next_pcp_appt_date (from Patient (LINK))"[0]::string) as next_pcp_appt_date,
        try_to_date(j:"next_careteam_appt_date (from Patient (LINK))"[0]::string) as next_careteam_appt_date,

        -- task master lookups
        j:"Created by (from Task Master (LINK))"[0]:"name"::string as tm_created_by_name,
        j:"Created by (from Task Master (LINK))"[0]:"id"::string as tm_created_by_id,
        j:"Created by (from Task Master (LINK))"[0]:"email"::string as tm_created_by_email,
        try_to_date(j:"TOC HV Request - Discharge Date (from Task Master (LINK))"[0]::string) as toc_hv_request_discharge_date,
        j:"TOC HV Request - Med Rec Needed? (from Task Master (LINK))"[0]::string as toc_hv_request_med_rec_needed,
        j:"TOC HV Request - Discharge Paperwork Needed? (from Task Master (LINK))"[0]::string as toc_hv_request_discharge_paperwork_needed,

        -- task info
        j:"Task Name"::string as task_name,
        j:"Interface Warp Link"::string as interface_warp_link,

        -- HV outcome fields
        try_to_timestamp_tz(j:"HV Planned Date & Time"::string) as hv_planned_datetime,
        j:"Nurse TOC Visit Outcome"::string as nurse_toc_visit_outcome,
        j:"Was patient available during home visit?"::string as was_patient_available,
        try_to_boolean(j:"Discharge Summary?"::string) as has_discharge_summary,
        try_to_boolean(j:"Did you call the patient?"::string) as did_call_patient,
        try_to_boolean(j:"Did you speak with the referring Nurse?"::string) as did_speak_with_referring_nurse,
        try_to_boolean(j:"Does patient need assistance with follow-up specialist appointments??"::string) as needs_followup_specialist_assistance,

        -- nested objects
        j:"Last Modified"::timestamp_tz as last_modified_at,
        j:"Last Modified By":"id"::string as last_modified_by_id,
        j:"Last Modified By":"email"::string as last_modified_by_email,
        j:"Last Modified By":"name"::string as last_modified_by_name,

        j:"Stage Last modified at"::timestamp_tz as stage_last_modified_at,
        j:"Stage Last modified by":"id"::string as stage_last_modified_by_id,
        j:"Stage Last modified by":"email"::string as stage_last_modified_by_email,
        j:"Stage Last modified by":"name"::string as stage_last_modified_by_name

    from parsing_data
)
select
    airtable_id,
    row_number() over (partition by airtable_id order by last_modified_at desc) as snapshot_rank,
    run_datetime,
    last_modified,
    home_visit_stage,
    home_visit_status,
    hv_completed_date,
    hv_planned_datetime,
    is_discharge_paperwork_needed,
    is_discharge_paperwork_needed_v2,
    tm_created_by_name,
    tm_created_by_id,
    tm_created_by_email,
    toc_hv_request_discharge_date,
    toc_hv_request_med_rec_needed,
    toc_hv_request_discharge_paperwork_needed,
    task_master_link_id,
    task_name,
    interface_warp_link,
    patient_link_id,
    patient_name,
    workflow_type,
    due_date,
    request_datetime,
    requestor_notes,
    location_name,
    provider_name,
    elation_id,
    elation_patient_url,
    roi_form_due_date,
    is_active_assignment,
    high_risk_patient,
    next_pcp_appt_date,
    next_careteam_appt_date,
    is_home_visit_completed,
    is_discharge_date_confirmed,
    medication_reconciliation_due,
    is_medications_available,
    medication_list_photo,
    discharge_summary_photo,
    signed_roi_photo,
    is_roi_completed,
    notes,
    compas_note,
    attachments,
    pcp_toc_visit_outcome,
    roi_due_alert,
    delay_reason,
    warp_button,
    nurse_toc_visit_outcome,
    was_patient_available,
    has_discharge_summary,
    did_call_patient,
    did_speak_with_referring_nurse,
    needs_followup_specialist_assistance,
    is_resolved,
    resolved_at,
    resolved_by_id,
    resolved_by_email,
    resolved_by_name,
    last_modified_at,
    last_modified_by_id,
    last_modified_by_email,
    last_modified_by_name,
    stage_last_modified_at,
    stage_last_modified_by_id,
    stage_last_modified_by_email,
    stage_last_modified_by_name
from type_conversion