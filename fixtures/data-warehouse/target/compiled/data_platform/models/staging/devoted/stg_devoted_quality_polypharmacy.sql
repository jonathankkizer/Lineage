select
	to_date(REPORTDATE) as report_date,
	DEVOTEDID as member_id,
	PATIENTNAME as patient_name,
	to_date(PATIENTBIRTHDATE) as patient_birth_date,
	-- various other patient-related columns available but also duplicative of membership files
	MEASURECATEGORY as measure_category,
	MEDICATIONCATEGORY as medication_category,
	svh_qm.quality_measure,
	svh_qm.quality_measure_type,
	svh_qm.measure_weight,
	MEASURENAME as payer_measure_name,
	GAPMEASURETYPE as gap_measure_type,
	GAPACTION as gap_action,
	CUMULATIVEDAYSOVERLAP as cumulative_days_overlap,
	PRIORYEARMEASURECOMPLIANCE as prior_year_measure_compliance,
	to_date(DATEFILLED) as date_filled,
	RXNUMBER as rx_number,
	PRODUCTNAME as product_name,
	to_boolean(ISGENERICPRODUCT) as is_generic_product,
	GENERICPRODUCTNAME as generic_product_name,
	QUANTITYDISPENSED as quantity_dispensed,
	DAYSSUPPLY as days_supply,
	FORMULARYTIER as formulary_tier,
	FILLNUMBER as fill_number,
	NUMBEROFREFILLS as number_of_refills,
	PRESCRIBERFIRSTNAME as prescriber_first_name,
	PRESCRIBERLASTNAME as prescriber_last_name,
	PRESCRIBERBILLINGGROUPS as prescriber_billing_groups,
	PRESCRIBERADDRESS as prescriber_address,
	PRESCRIBERADDRESSCITY as prescriber_address_city,
	PHARMACYIDTYPE as pharmacy_id_type,
	PHARMACYNAME as pharmacy_name,
	PHARMACYADDRESSLINE1 as pharmacy_address_line_1,
	PHARMACYADDRESSLINE2 as pharmacy_address_line_2,
	PHARMACYCITY as pharmacy_city,
	PHARMACYSTATE as pharmacy_state,
	PHARMACYPHONE as pharmacy_phone,
	split(_ab_source_file_url, '/')[array_size(split(_ab_source_file_url, '/'))-1]::varchar as src_file_name,
	'Devoted' as source,
from airbyte_source_prod.devoted.quality_polypharmacy dm
left join dw_dev.dev_jkizer_staging.stg_star_payer_measure_names svh_qm
	on dm.MEASURENAME = svh_qm.payer_measure_name
	and svh_qm.payer_name = 'Devoted'