select
	date_id,
	date(date_day) as date_day,
	date(date_week) as date_week,
	date(date_month) as date_month,
	date(date_quarter) as date_quarter,
	date(date_year) as date_year,
	to_number(calendar_year) as calendar_year,
	to_number(calendar_month) as calendar_month,
	to_number(calendar_week) as calendar_week,
	to_number(fiscal_year) as fiscal_year,
	to_number(fiscal_month) as fiscal_month,
	to_number(fiscal_week) as fiscal_week,
	to_number(day_of_month) as day_of_month,
	to_number(day_of_week) as day_of_week,
	to_boolean(is_bow) as is_bow,
	to_boolean(is_bom) as is_bom,
	to_boolean(is_eow) as is_eow,
	to_boolean(is_eom) as is_eom,
	to_number(calendar_quarter) as calendar_quarter,
	to_number(fiscal_quarter) as fiscal_quarter,
	to_boolean(is_weekend) as is_weekend,
	to_boolean(is_holiday) as is_holiday,
	to_boolean(is_workday) as is_workday
from source_prod.misc.src_dim_date