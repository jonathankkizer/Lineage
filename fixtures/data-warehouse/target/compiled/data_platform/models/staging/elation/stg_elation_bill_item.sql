select
	UQ_BILL_ITEM as uq_bill_item,
	ID as bill_item_id,
	BILL_ID as bill_id,
	CPT as cpt_code,
	SEQNO as bill_sequence_no,
	date(LAST_MODIFIED) as last_modified_date,
	to_timestamp(LAST_MODIFIED) as last_modified_datetime,
	MODIFIER_1 as modifier_1,
	MODIFIER_2 as modifier_2,
	MODIFIER_3 as modifier_3,
	MODIFIER_4 as modifier_4,
	UNITS as units,
	UNIT_CHARGE as unit_charge,
	date(CREATION_TIME) as creation_date,
	to_timestamp(CREATION_TIME) as creation_datetime,
	CREATED_BY_USER_ID as created_by_user_id,
	date(DELETION_TIME) as _deletion_date,
	to_timestamp(DELETION_TIME) as deletion_datetime,
	DELETED_BY_USER_ID as _deleted_by_user_id,
	to_boolean(case when DELETION_TIME is not null then 1 else 0 end) as _is_deleted_record,
	-- WAREHOUSE_ID
	to_timestamp(HDB_LAST_SYNC) as hdb_last_sync_datetime,
	date(HDB_LAST_SYNC) as _last_sync_date,
	row_number() over (partition by ID order by HDB_LAST_SYNC desc) as _idx
from elationhealth_ehdw_azure_scentralus_texas_elation_suvida_snowflake_secure_share.suvida.bill_item